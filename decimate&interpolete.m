x=[1,8,7,4,5,7,7,0,2,5,7,4,5,0,1,6,7,4];
n=0:length(x)-1;
subplot(4,1,1);
stem(n,x);
xlabel("n");
ylabel("x[n]");
title("original");
y_down=decimate(x,2,5);
m=0:length(y_down)-1;
subplot(4,1,2);
stem(m,y_down);
xlabel("m");
ylabel("x[m]");
title("decimate");
y_up=interp(x,2,5);
m=0:2*length(x)-1;
subplot(4,1,3);
stem(m,y_up);
xlabel("m");
ylabel("x[m]");
title("interploate");