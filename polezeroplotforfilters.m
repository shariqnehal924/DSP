n=-10:10;
fc=200;
fs=1000;
w=fc/fs;
h=fir1(50,w);
[H,W]=freqz(h,1,1024);
subplot(211);
plot(fs*W/pi,abs(H));
title("Frequency response");
xlabel("f");
ylabel("|H(e^j^w)|");
subplot(212);
zplane(h,1);
title("pole zero plot");
